        string path = Path.Combine(LoaderEnvironment.GameRootDirectory, "itemsid.txt");
        using StreamWriter writer = new(path, false);
        foreach (var item in ItemDatabaseManager.Items) writer.WriteLine($"{item.Name} = {item.Id},");
        return true;
